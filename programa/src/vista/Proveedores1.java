    public void AgregarProveedor() {

       String SQL = "INSERT INTO proveedor (Rut,Razon social,Direccion,Ciudad,Telefono,Mail,Nombre contacto,Productos,Cuenta bancaria,Estado) values (?,?,?,?,?,?,?,?,?,?)";

        try {

            PreparedStatement ps = conect.prepareStatement(SQL);
            ps.setString(1, txtRut.getText());
            ps.setString(2, txtRS.getText());
            ps.setString(3, txtDireccion.getText());
            ps.setString(4, txtCiudad.getText());
            ps.setString(5, txtTelefono.getText());
            ps.setString(6, txtMail.getText());
            ps.setString(7, txtContacto.getText());
            ps.setString(8, txtProductos.getText());
            ps.setString(9, txtCuentaBancaria.getText());
            ps.setString(10, txtEstado.getText());

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Ingresado correctamente!!");

        } catch (HeadlessException | SQLException e) {

            JOptionPane.showMessageDialog(null, "Error de registro !!");

        }
    }
